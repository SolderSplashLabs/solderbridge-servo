#include "LPC11Uxx.h"
#include "DataTypes.h"

void LowPowerInit ( void )
{
	LPC_IOCON->RESET_PIO0_0 = 0;

	// Initialise all Pins as gpio with inactive pull up/downs
	LPC_IOCON->PIO0_1 = 0;
	LPC_IOCON->PIO0_2 = 0;
	LPC_IOCON->PIO0_3 = 0;
	LPC_IOCON->PIO0_4 = 0;
	LPC_IOCON->PIO0_5 = 0;
	LPC_IOCON->PIO0_6 = 0;
	LPC_IOCON->PIO0_7 = 0;
	LPC_IOCON->PIO0_8 = 0;
	LPC_IOCON->PIO0_9 = 0;

	LPC_IOCON->SWCLK_PIO0_10 = 0;
	// DIsable ADMODE, Set as GPIO
	LPC_IOCON->TDI_PIO0_11 = 1 | (1<<7);
	LPC_IOCON->TMS_PIO0_12 = 1 | (1<<7);
	LPC_IOCON->TDO_PIO0_13 = 1 | (1<<7);
	LPC_IOCON->TRST_PIO0_14 = 1 | (1<<7);

	LPC_IOCON->SWDIO_PIO0_15 = (1<<7);

	LPC_IOCON->PIO0_16 = (1<<7);;
	LPC_IOCON->PIO0_17 = 0;
	LPC_IOCON->PIO0_18 = 0;
	LPC_IOCON->PIO0_19 = 0;
	LPC_IOCON->PIO0_20 = 0;
	LPC_IOCON->PIO0_21 = 0;

	// PIO Mode
	LPC_IOCON->PIO0_22 = (1<<7);
	LPC_IOCON->PIO0_23 = (1<<7);

	LPC_IOCON->PIO1_0 = 0;
	LPC_IOCON->PIO1_1 = 0;
	LPC_IOCON->PIO1_2 = 0;
	LPC_IOCON->PIO1_3 = 0;
	LPC_IOCON->PIO1_4 = 0;
	LPC_IOCON->PIO1_5 = 0;
	LPC_IOCON->PIO1_6 = 0;
	LPC_IOCON->PIO1_7 = 0;
	LPC_IOCON->PIO1_8 = 0;
	LPC_IOCON->PIO1_9 = 0;
	LPC_IOCON->PIO1_10 = 0;
	LPC_IOCON->PIO1_11 = 0;
	LPC_IOCON->PIO1_12 = 0;
	LPC_IOCON->PIO1_13 = 0;
	LPC_IOCON->PIO1_14 = 0;
	LPC_IOCON->PIO1_15 = 0;
	LPC_IOCON->PIO1_16 = 0;
	LPC_IOCON->PIO1_17 = 0;
	LPC_IOCON->PIO1_18 = 0;
	LPC_IOCON->PIO1_19 = 0;
	LPC_IOCON->PIO1_20 = 0;
	LPC_IOCON->PIO1_21 = 0;
	LPC_IOCON->PIO1_22 = 0;
	LPC_IOCON->PIO1_23 = 0;
	LPC_IOCON->PIO1_24 = 0;
	LPC_IOCON->PIO1_25 = 0;
	LPC_IOCON->PIO1_26 = 0;
	LPC_IOCON->PIO1_27 = 0;
	LPC_IOCON->PIO1_28 = 0;
	LPC_IOCON->PIO1_29 = 0;
	LPC_IOCON->PIO1_30 = 0;
	LPC_IOCON->PIO1_31 = 0;
}
